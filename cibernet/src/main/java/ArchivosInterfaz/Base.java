    private void btn_CosteoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_CosteoMouseClicked
        // TODO add your handling code here:
        double Psm = ((Number) CEMP.getValue()).doubleValue();
        double Prm = ((Number) CRMP.getValue()).doubleValue();
        double Crm = ((Number) CRMC.getValue()).doubleValue();
        double Csm = ((Number) CEMC.getValue()).doubleValue();
        //Fin Costeo Materiales
        double Psmo = ((Number) CEMOCP.getValue()).doubleValue();
        double Prmo = ((Number) CRMOP.getValue()).doubleValue();
        double Crmo = ((Number) CRMOC.getValue()).doubleValue();
        double Csmo = ((Number) CEMOC.getValue()).doubleValue();
        //Fin Costeo Mano de Obra
        double Psci = ((Number) CEIP.getValue()).doubleValue();
        double Prci = ((Number) CRIP.getValue()).doubleValue();
        double Crci = ((Number) CRIC.getValue()).doubleValue();
        double Csci = ((Number) CEIC.getValue()).doubleValue();
        
        double vm = ((Psm-Prm)*Crm)+((Csm-Crm)*Psm);
        double vmo = ((Psmo-Prmo)*Crmo)+((Csmo-Crmo)*Psmo);
        double vci = ((Psci-Prci)*Crci)+((Csci-Crci)*Psci);
        BigDecimal vm1 = new BigDecimal(vm).setScale(2, RoundingMode.HALF_UP);
        vm=vm1.doubleValue();
        BigDecimal vmo1 = new BigDecimal(vmo).setScale(2, RoundingMode.HALF_UP);
        vmo=vmo1.doubleValue();
        BigDecimal vc1 = new BigDecimal(vci).setScale(2, RoundingMode.HALF_UP);
        vci=vc1.doubleValue();
        
        rm.setText("Resultado Materiales: "+vm);
        rmo.setText("Resultado Materiales: "+vmo);
        rci.setText("Resultado Materiales: "+vci);
        
        
    }//GEN-LAST:event_btn_CosteoMouseClicked

