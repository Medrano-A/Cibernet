    private void btn_cccMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_cccMouseClicked
        // TODO add your handling code here:
        List<Cuenta> ListaCuenta = CM.cargarCuentas();
        for(Cuenta c: ListaCuenta) {
            c.cerrarCuenta();
        }
        try {
            CM.guardarCuentas(ListaCuenta);
        } catch (IOException ex) {
            Logger.getLogger(Base.class.getName()).log(Level.SEVERE, null, ex);
        }
        // IDs específicos de las cuentas que queremos mostrar
        String[] idsFiltrados = {"4101", "4102", "4103", "4104", "5101", "5201", "5202", "5203", "5204", "5205", "5206", "5207"};

        // Filtrar las cuentas con los IDs específicos
        List<Cuenta> cuentasFiltradas = ListaCuenta.stream()
                .filter(cuenta -> java.util.Arrays.asList(idsFiltrados).contains(cuenta.getIdCuenta()))
                .collect(Collectors.toList());

        // Crear el modelo de la tabla con las columnas Cuenta, Debe, Haber
        DefaultTableModel model = new DefaultTableModel(new Object[]{"Cuenta", "Debe", "Haber"}, 0);

        // Variables para calcular los totales de "Debe" y "Haber"
        double totalDebe = 0;
        double totalHaber = 0;

        // Agregar las cuentas filtradas al modelo de la tabla y calcular los totales
        for (Cuenta cuenta : cuentasFiltradas) {
            // Asegurarse de usar el nombre de la cuenta
            model.addRow(new Object[]{cuenta.getNombre(), cuenta.getDebe(), cuenta.getHaber()});
            totalDebe += cuenta.getDebe();
            totalHaber += cuenta.getHaber();
        }

        // Agregar la fila "Total" con la suma de las columnas Debe y Haber
        model.addRow(new Object[]{"Total", totalDebe, totalHaber});

        // Calcular la utilidad como Haber - Debe y agregarla en la última fila
        double utilidad = totalHaber - totalDebe;
        model.addRow(new Object[]{"Utilidad", "", utilidad});
        resultados_hoja.setModel(model);
        
        
        
        // Actualizar Utilidad y crear el balance general
        //recuperando las cuentas
        List<Cuenta> ListaCuenta2 = CM.cargarCuentas();
        //Buscando y guardando
        for (Cuenta cuenta : ListaCuenta2) {
            // Asegurarse de usar el nombre de la cuenta
            if(cuenta.getIdCuenta().equals("3102")){
                cuenta.setHaber(utilidad);
            }
        }
        //Guardando
        try {
            CM.guardarCuentas(ListaCuenta);
        } catch (IOException ex) {
            Logger.getLogger(Base.class.getName()).log(Level.SEVERE, null, ex);
        }
        List<Cuenta> ListaCuenta1 = CM.cargarCuentas();
        BalanceGeneralTableModel tablemodelC= new BalanceGeneralTableModel(ListaCuenta1);
        general_hoja.setModel(tablemodelC);
        LibroMayorTableModel tablemodel1 = new LibroMayorTableModel(ListaCuenta1);
        libro_mayor.setModel(tablemodel1);
        
    }//GEN-LAST:event_btn_cccMouseClicked

